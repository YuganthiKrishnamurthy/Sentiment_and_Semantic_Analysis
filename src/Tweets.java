public class Tweets {
    String tweetPhrase;
    String twitterMetadata;
    String isItRT;

    public String getTweetPhrase() {
        return tweetPhrase;
    }

    public void setTweetPhrase(String tweetPhrase) {
        this.tweetPhrase = tweetPhrase;
    }

    public String getTwitterMetadata() {
        return twitterMetadata;
    }

    public void setTwitterMetadata(String twitterMetadata) {
        this.twitterMetadata = twitterMetadata;
    }

    public String getIsItRT() {
        return isItRT;
    }

    public void setIsItRT(String isItRT) {
        this.isItRT = isItRT;
    }
}
